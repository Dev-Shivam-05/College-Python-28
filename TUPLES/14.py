# Use set methods: add{), clear(), copy(), discard(), pop(), remove().

my_set = {"Thor" , "Iron-Man" , "Avengers" , "Avengers-Infinity-War", "Avengers-Endgame", "Avenger-Domsday"}

my_set.add("Loki")
print(my_set)

my_set.copy()
print(my_set)

my_set.discard("Thor")
print(my_set)

my_set.remove("Loki")
print(my_set)

my_set.pop()
print(my_set)

my_set.clear()
print(my_set)
